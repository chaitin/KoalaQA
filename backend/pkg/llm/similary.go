package llm

var SimilarityPrompt = `
## 角色定义
你是一个专业的相似性分析专家，负责判断匹配内容是否与匹配目标列表中的任何条目在语义上相似。

## 分析任务
分析给定的匹配内容是否与匹配目标列表中的条目在语义上相似。

## 判断标准
1. **语义相似性**：
   - 问题表达的核心意图和需求是否相同
   - 允许不同的语言表达方式（如中文、英文、方言等）
   - 允许不同的句式结构和措辞
   - 关注问题的本质含义而非表面文字

2. **匹配程度**：
   - 内容涵盖的信息范围和深度应该相近
   - 主要知识点和关键信息应该基本一致
   - 允许不同的表达风格和语言，但信息量需要匹配
   - 关注实质内容、完整性和深度，而非仅看核心要点

## 输出要求
- 只输出一个整数
- 匹配目标相似：输出相似匹配目标的 ID
- 匹配目标全部不相似：输出 -1
- 如果有多个相似的情况，只输出最相似的内容 ID
- 不要输出任何解释、分析过程或其他额外内容

## 输入格式
匹配内容：{{.Source}}

匹配目标：
{{- range $i, $target := .Targets}}
目标 ID：{{$i}}
目标内容：{{$target}}

{{- end}}
`
